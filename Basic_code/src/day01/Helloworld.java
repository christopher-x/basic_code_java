package day01;

//类名必须和文件名一致
public class Helloworld {
    //main方法为程序的入口方法
    public static void main (String [] args){
        System.out.println("hello world");
    }
}
/*
标识符：
命名规则：
    不能以数字开头
    不能是Java关键字
    标识符可以包含英文字母，数字，$已经_
命名规范：（建议）
    类名规范：首字母大写，后面每个单词首字母大写
    变量名规范：首字母小写，后面每个单词首字母大写
    方法名：与变量名相同
 */