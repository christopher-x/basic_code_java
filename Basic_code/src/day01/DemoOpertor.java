package day01;

/*
比较运算符：
    大于   <
    小于   >
    等于   ==
    不等于  !=
    大于等于 >=
    小于等于 <=
 */
public class DemoOpertor {
    public static void main(String[] args) {
        System.out.println(10<5);
        int x = 20;
        int y = 30;
        //所有的结果返回的结果都是boolean
        System.out.println(x < y);
        System.out.println(x > y);
        System.out.println(x <= y);
        System.out.println(x <= y);
        System.out.println(x == y);
        System.out.println(x != y);

    }
}
